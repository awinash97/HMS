const mongoose = require('mongoose')

const employeeSchema = new mongoose.Schema({
    name : {
        type : String,
        required : true
    },
    email : {
        type : String,
        required : true
    },
    phone : {
        type : Number,
        required : true
    },
    password : {
        type : String,
        required : true
    },
    cpassword : {
        type : String,
        required : true
    },
})

const User = mongoose.model('EMPLOYEE', employeeSchema);

module.exports = User;